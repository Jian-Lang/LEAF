def configure_logger():
    # Remove all existing handlers
    logger.remove()
    # Add file handler for DEBUG level
    logger.add("log/generate_knowledge.log", retention="10 days", level="DEBUG")
    # Add console handler for INFO level
    logger.add(sys.stdout, level="INFO")
    return logger


if __name__ == "__main__":
    # Set up multiprocessing
    mp.set_start_method("spawn")
    parser = ArgumentParser()
    parser.add_argument("--dataset", type=str)
    args = parser.parse_args()
    datasets = [args.dataset]
    max_processes = 6

    # Configure logger
    configure_logger()

    for dataset in datasets:
        logger.info(f"Processing {dataset}")
        # read the json file
        data_df = pd.read_json(f"data/{dataset}/data.jsonl", lines=True)
        train_vids = pd.read_csv(f"data/{dataset}/vids/train.csv").iloc[:, 0].tolist()
        valid_vids = pd.read_csv(f"data/{dataset}/vids/valid.csv").iloc[:, 0].tolist()
        data_df = data_df[data_df["vid"].isin(train_vids + valid_vids)]
        save_path = Path(f"result/knowledge/{dataset}/knowledge_pure.jsonl")

        # create the directory if not exists
        save_path.parent.mkdir(parents=True, exist_ok=True)

        # Check if save file exists and filter out already processed videos
        if save_path.exists() and save_path.stat().st_size > 0:
            existing_df = pd.read_json(save_path, lines=True)
            processed_vids = set(existing_df["vid"].values)
            original_count = len(data_df)
            data_df = data_df[~data_df["vid"].isin(processed_vids)]
            logger.info(
                f"Found {len(processed_vids)} already processed videos. Processing {len(data_df)} remaining videos (total: {original_count})"
            )
        else:
            logger.info(f"No existing save file found. Processing all {len(data_df)} videos")

        # Skip if all videos are already processed
        if len(data_df) == 0:
            logger.info(f"All videos for {dataset} have been processed. Skipping.")
            continue

        # Initialize empty dataframe for saving results
        save_df = pd.DataFrame(
            columns=[
                "vid",
                "label",
                "prediction",
                "description",
                "explanation",
                "fix_label",
                "fix_label_explanation",
                "fix_prediction",
                "fix_grounding",
                "fix_grounding_explanation",
            ]
        )

        # Set up multiprocessing components
        with Manager() as manager:
            save_lock = manager.Lock()
            pbar_update = manager.Value("i", 0)

            # Create a process pool
            pool = mp.Pool(processes=max_processes, initializer=configure_logger)

            # Set up the progress bar
            pbar = tqdm(total=len(data_df), desc=f"Processing {dataset}")

            # Create a partial function with fixed arguments
            process_func = partial(
                process_video,
                dataset=dataset,
                save_lock=save_lock,
                save_path=save_path,
                save_df=save_df,
                pbar_update=pbar_update,
            )

            # Start processing in parallel
            results = []
            for index, row in data_df.iterrows():
                vid = row["vid"]
                results.append(pool.apply_async(process_func, args=(vid, row)))

            # Monitor progress
            completed = 0
            while completed < len(results):
                new_completed = sum(1 for r in results if r.ready())
                if new_completed > completed:
                    pbar.n = pbar_update.value
                    pbar.refresh()
                    completed = new_completed
                time.sleep(0.1)

            # Close the pool and wait for all processes to finish
            pool.close()
            pool.join()

            # Final update of progress bar
            pbar.n = pbar_update.value
            pbar.refresh()
            pbar.close()

            logger.info(f"Completed processing {dataset}")
