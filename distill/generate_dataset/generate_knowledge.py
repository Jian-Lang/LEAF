def check_by_grounding(vid, images, title, transcript, ocr, description, explanation, prediction):
    logger.debug(f"Entering check_by_grounding for video ID: {vid}")
    # check the explanation by grounding
    # English prompt for checking if the explanation is grounded in the original video
    if prediction == 1:
        prediction = "hateful"
    else:
        prediction = "benign"

    prompt = f"""
You are a moderator on a video platform.

Given the following information:
- 16 consecutive video frames (visual context),
- Video Title: [{title}]
- Video Transcript: [{transcript}]
- On-screen Text (OCR): [{ocr}]
- Video Description: [{description}]
- Your Previous Explanation: [{explanation}]

The video has been classified as {prediction} by both the platform standard and your previous prediction.

Hateful video definition: A video is hateful if it targets individuals or groups based on race, religion, gender, sexuality, or other identities.

Your ONLY task is to verify if your previous explanation faithfully matches the prediction based on the video content.

If your previous explanation accurately reflects the prediction, respond ONLY with:
"OK"

ONLY IF your previous explanation does NOT faithfully match the prediction, provide a revised explanation.  

In this case, clearly provide a brief, faithful, and easy-to-understand revised explanation WITHOUT changing the prediction.

Do NOT provide any analysis, reasoning or other comments if your original explanation is already faithful.

Your response must strictly follow ONE of these formats:

If faithful:  
OK

If not faithful:  
Respond in the following format: 
Revised Explanation: [your brief and clear revised explanation]
Answer: [{prediction}]

"""

    logger.debug(f"Request: {prompt.replace(chr(10), ' ')}")
    response = api_model.chat_multi_img(prompt, images)
    logger.debug(f"Response: {response}")

    if "OK" in response:
        fix_grounding = 0
    else:
        fix_grounding = 1
        explanation = response.split("Revised Explanation:")[-1].split("Answer:")[0].strip()

    return explanation, fix_grounding


def process_video(
    vid,
    row,
    dataset,
    save_lock,
    save_path,
    save_df,
    pbar_update,
):
    try:
        title = row["title"]
        ocr = row["ocr"][:10000]
        transcript = row["transcript"][:10000]
        label = row["label"]
        image_paths = [f"data/{dataset}/frames_16/{vid}/frame_{i:03d}.jpg" for i in range(0, 16)]
        images = [Image.open(path).convert("RGB") for path in image_paths]

        # Process everything from scratch
        description = generate_video_description(vid, images, title, transcript, ocr)
        ori_explanation, prediction = generate_explanation(vid, images, title, transcript, ocr, description)
        if prediction == label:
            fix_label = 0
            fix_label_explanation = ori_explanation
            fix_prediction = prediction
        else:
            fix_label = 1
            fix_label_explanation, fix_prediction = fix_by_label(
                vid, images, label, title, transcript, ocr, description, ori_explanation
            )

        fix_grounding_explanation, fix_grounding = check_by_grounding(
            vid, images, title, transcript, ocr, description, fix_label_explanation, prediction=label
        )
        ic(f"Finish for {vid}")

        # Save results with lock to prevent race conditions
        with save_lock:
            current_save_df = pd.read_json(save_path, lines=True) if save_path.exists() else save_df.copy()
            new_row = pd.DataFrame(
                {
                    "vid": [vid],
                    "label": [label],
                    "prediction": [prediction],
                    "description": [description],
                    "explanation": [ori_explanation],
                    "fix_label": [fix_label],
                    "fix_label_explanation": [fix_label_explanation],
                    "fix_prediction": [fix_prediction],
                    "fix_grounding": [fix_grounding],
                    "fix_grounding_explanation": [fix_grounding_explanation],
                }
            )
            updated_df = pd.concat([current_save_df, new_row], ignore_index=True)
            updated_df.to_json(save_path, orient="records", lines=True)
            pbar_update.value += 1
            logger.info(f"Processed video {vid} for {dataset}, progress: {pbar_update.value}")

        return True
    except Exception as e:
        logger.error(f"Error processing {vid}: {str(e)}")
        return False


